/**
 * Express router for managing and retrieving books.
 * Includes routes for creating, getting, updating, and deleting books,
 * as well as retrieving books by a specific author.
 *
 * Uses authentication middleware to enforce permissions for different actions.
 *
 * @swagger
 * tags:
 *   name: Books
 *   description: Book management and retrieval
 */

import express from 'express';
import validate from '../../middlewares/validate';
import { bookValidation } from '../../validations';
import { bookController } from '../../controllers';
import auth from '../../middlewares/auth';

const router = express.Router();

router
  .route('/')
  .post(auth('manageBooks'), validate(bookValidation.createBook), bookController.createBook)
  .get(auth('getBooks'), validate(bookValidation.getBooks), bookController.getBooks);

router
  .route('/:bookId')
  .get(auth('getBooks'), validate(bookValidation.getBook), bookController.getBook)
  .patch(auth('manageBooks'), validate(bookValidation.updateBook), bookController.updateBook)
  .delete(auth('manageBooks'), validate(bookValidation.deleteBook), bookController.deleteBook);

router
  .route('/author/:authorId')
  .get(
    auth('getBooks'),
    validate(bookValidation.getBooksByAuthor),
    bookController.getBooksByAuthor
  );

export default router;

